			b. Asesmen Tidak Langsung; dilakukan melalui pengisian kuisioner tentang persepsi mahasiswa terhadap pencapaian CPMK
			</p>
			<p>
			<b>2. Asesmen Capaian Pembelajaran Lulusan (CPL)</b><br> 
			</p>
