			<b>SiPenCapil (Sistem Pengukuran Capaian Pembelajaran Prodi Teknik Sipil dan Lingkungan) </b> adalah sistem penunjang program perbaikan mutu pendidikan berkelanjutan PS SIL. Sistem ini mencakup desain asesmen capaian pembelajaran, pengumpulan data dan analisis hasil pengukuran, evaluasi dan tindak lanjut, serta monitoring efektivitas perbaikan yang dilaksanakan, baik pada level mata kuliah (CPMK), program studi (CPL), maupun profesional (PPM).  
