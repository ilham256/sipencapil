			b. Asesmen Tidak langsung; dimaksudkan untuk mengukur persepsi lulusan terhadap pencapaian CPL yang dilakukan menjelang kelulusan (exit survey)
			</p>
			<p>
			<b>3. Evaluasi relevansi CPL dan Profil Profesional Mandiri (PPM)</b><br> 
			</p>
